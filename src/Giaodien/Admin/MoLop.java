    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        if(this.txtClassID.getText().length()==0)
            JOptionPane.showMessageDialog(null, "Mã lớp không được bỏ trống","Thông báo",1);
        else if (this.txtClassName.getText().length()==0)
            JOptionPane.showMessageDialog(null, "Tên lớp không được bỏ trống","Thong bao",1);
        else {
            int classid = Integer.parseInt(this.txtClassID.getText());
            try {
                Lophoc lophoc = new Lophoc(kihoc, monhoc, classid, this.txtClassName.getText());
                Admin.admin.openClass(lophoc);
                UpdataTable.LoadData(sql1, tbClass);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            
        }
    }//GEN-LAST:event_btAddActionPerformed

