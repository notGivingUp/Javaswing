    public void LoadCB_Kihoc(String sql){
        DefaultComboBoxModel model1=new DefaultComboBoxModel();
        try {
            pst = Giaodien.Main.conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            while(rs.next()){
                //this.jComboBox1.addItem(rs.getInt(1));
                model1.addElement(rs.getInt(1));
            }
            this.jComboBox1.setModel(model1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void LoadCB_Monhoc(int sj_id){
        DefaultComboBoxModel model=new DefaultComboBoxModel();
        String sql = "select subject_id from subject where semester = '"+sj_id+"'";
        try {
            pst = Giaodien.Main.conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                //this.jComboBox2.addItem(rs.getString(1));
                model.addElement(rs.getString(1));
            }
            this.jComboBox2.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
